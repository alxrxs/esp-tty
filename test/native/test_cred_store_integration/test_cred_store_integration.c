/*
 * test_cred_store_integration.c -- cred_store integration test with
 * a real DER cert produced by the Python FakeNdesCA harness.
 *
 * Tests:
 *   1. cred_store_parse_not_after() on a cert issued by FakeNdesCA returns
 *      the epoch that matches Python's calendar.timegm computation.
 *   2. Save {cert, key, ca_chain} -> Load -> byte-for-byte equality on all
 *      fields, not_after matches fixture epoch.
 *   3. Parse after clear returns NOT_FOUND.
 *   4. parse_not_after returns correct epoch even without saving.
 *   5. save/load preserves large ca_chain blob without truncation.
 *
 * Fixtures are generated by test/scripts/test_scep_protocol_e2e.py's
 * FakeNdesCA (run once, committed to the repo).  To regenerate:
 *
 *   cd /root/esp-tty
 *   venv/bin/python test/scripts/regen_cred_store_fixtures.py
 *
 * Design choice: pre-committed fixtures rather than a shell wrapper because:
 *   - Keeps the native test hermetic (no Python dependency at test-run time).
 *   - Avoids brittle pipe between Python and PlatformIO's test runner.
 *   - Fixtures capture a concrete issuer cert, making assertion failures easier
 *     to diagnose than with random-key fixtures generated on every run.
 *
 * Compiled with:
 *   -DCRED_STORE_NATIVE_TEST  (in-memory NVS backend)
 *   -DUNIT_TEST
 */

