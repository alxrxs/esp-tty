/* Expected NotAfter epoch: 2027-05-13 21:52:30 UTC */
#define FIXTURE_NOT_AFTER_EPOCH  UINT64_C(1810245150)

/* A small fake CA chain (PEM-like string for testing storage) */
static const char fixture_ca_chain[] =
    "-----BEGIN CERTIFICATE-----\n"
    "MIIBMzCB2qADAgECAgRl4...SCEP-Integration-Test-CA\n"
    "-----END CERTIFICATE-----\n";

/* --------------------------------------------------------------------------
 * setUp / tearDown
 * -------------------------------------------------------------------------- */
void setUp(void)    { cred_store_clear(); }
void tearDown(void) {}

/* --------------------------------------------------------------------------
 * Test 1: parse_not_after on FakeNdesCA-issued cert returns correct epoch
 * -------------------------------------------------------------------------- */
static void test_parse_not_after_fake_ndes_ca_cert(void)
{
    uint64_t epoch = 0;
    esp_err_t err = cred_store_parse_not_after(fixture_cert_der,
                                               fixture_cert_der_len,
                                               &epoch);
    TEST_ASSERT_EQUAL_INT(ESP_OK, err);
    TEST_ASSERT_EQUAL_UINT64(FIXTURE_NOT_AFTER_EPOCH, epoch);
}

/* --------------------------------------------------------------------------
 * Test 2: save + load preserves cert and key byte-for-byte
 * -------------------------------------------------------------------------- */
static void test_save_load_real_cert_and_key(void)
{
    cred_store_t in;
    memset(&in, 0, sizeof(in));

    /* Device key */
    TEST_ASSERT_LESS_OR_EQUAL(CRED_DEV_KEY_MAX, fixture_key_der_len);
    in.dev_key_len = fixture_key_der_len;
    memcpy(in.dev_key, fixture_key_der, fixture_key_der_len);

    /* Device cert */
    TEST_ASSERT_LESS_OR_EQUAL(CRED_DEV_CERT_MAX, fixture_cert_der_len);
    in.dev_cert_len = fixture_cert_der_len;
    memcpy(in.dev_cert, fixture_cert_der, fixture_cert_der_len);

    /* CA chain */
    size_t chain_len = strlen(fixture_ca_chain);
    TEST_ASSERT_LESS_OR_EQUAL(CRED_CA_CHAIN_MAX, chain_len);
    in.ca_chain_len = chain_len;
    memcpy(in.ca_chain, fixture_ca_chain, chain_len);

    /* not_after from parsed cert */
    in.not_after = FIXTURE_NOT_AFTER_EPOCH;

    esp_err_t err = cred_store_save(&in);
    TEST_ASSERT_EQUAL_INT(ESP_OK, err);

    cred_store_t out;
    memset(&out, 0, sizeof(out));
    err = cred_store_load(&out);
    TEST_ASSERT_EQUAL_INT(ESP_OK, err);

    /* Verify key byte-for-byte */
    TEST_ASSERT_EQUAL_size_t(fixture_key_der_len, out.dev_key_len);
    TEST_ASSERT_EQUAL_MEMORY(fixture_key_der, out.dev_key, fixture_key_der_len);

    /* Verify cert byte-for-byte */
    TEST_ASSERT_EQUAL_size_t(fixture_cert_der_len, out.dev_cert_len);
    TEST_ASSERT_EQUAL_MEMORY(fixture_cert_der, out.dev_cert, fixture_cert_der_len);

    /* Verify CA chain */
    TEST_ASSERT_EQUAL_size_t(chain_len, out.ca_chain_len);
    TEST_ASSERT_EQUAL_MEMORY(fixture_ca_chain, out.ca_chain, chain_len);

    /* Verify not_after */
    TEST_ASSERT_EQUAL_UINT64(FIXTURE_NOT_AFTER_EPOCH, out.not_after);
}

/* --------------------------------------------------------------------------
 * Test 3: clear after save -> load returns NOT_FOUND
 * -------------------------------------------------------------------------- */
static void test_clear_after_real_cert_save(void)
{
    cred_store_t in;
    memset(&in, 0, sizeof(in));
    in.dev_cert_len = fixture_cert_der_len;
    memcpy(in.dev_cert, fixture_cert_der, fixture_cert_der_len);
    in.dev_key_len  = fixture_key_der_len;
    memcpy(in.dev_key, fixture_key_der, fixture_key_der_len);
    in.ca_chain_len = 1; in.ca_chain[0] = 0xAB;
    in.not_after    = FIXTURE_NOT_AFTER_EPOCH;

    TEST_ASSERT_EQUAL_INT(ESP_OK, cred_store_save(&in));

    /* Verify it's there */
    cred_store_t out;
    TEST_ASSERT_EQUAL_INT(ESP_OK, cred_store_load(&out));

    /* Clear */
    TEST_ASSERT_EQUAL_INT(ESP_OK, cred_store_clear());

    /* Load must fail */
    TEST_ASSERT_EQUAL_INT(ESP_ERR_NVS_NOT_FOUND, cred_store_load(&out));
}

/* --------------------------------------------------------------------------
 * Test 4: parse_not_after is pure (doesn't require save/load)
 * -------------------------------------------------------------------------- */
static void test_parse_not_after_is_pure_function(void)
{
    /* Don't call cred_store_save -- parse should work on raw DER regardless */
    uint64_t epoch = 0;
    esp_err_t err = cred_store_parse_not_after(fixture_cert_der,
                                               fixture_cert_der_len,
                                               &epoch);
    TEST_ASSERT_EQUAL_INT(ESP_OK, err);
    TEST_ASSERT_EQUAL_UINT64(FIXTURE_NOT_AFTER_EPOCH, epoch);

    /* Load should still return NOT_FOUND (no save was called) */
    cred_store_t out;
    TEST_ASSERT_EQUAL_INT(ESP_ERR_NVS_NOT_FOUND, cred_store_load(&out));
}

/* --------------------------------------------------------------------------
 * Test 5: save/load preserves large ca_chain blob without truncation
 * -------------------------------------------------------------------------- */
static void test_large_ca_chain_preserved(void)
{
    /* Build a ca_chain close to CRED_CA_CHAIN_MAX */
    cred_store_t in;
    memset(&in, 0, sizeof(in));
    in.dev_cert_len = fixture_cert_der_len;
    memcpy(in.dev_cert, fixture_cert_der, fixture_cert_der_len);
    in.dev_key_len  = fixture_key_der_len;
    memcpy(in.dev_key, fixture_key_der, fixture_key_der_len);

    /* Fill ca_chain with a recognizable pattern up to 1 KB */
    size_t chain_sz = 1024;
    for (size_t i = 0; i < chain_sz; i++) {
        in.ca_chain[i] = (uint8_t)(i & 0xFF);
    }
    in.ca_chain_len = chain_sz;
    in.not_after    = FIXTURE_NOT_AFTER_EPOCH;

    TEST_ASSERT_EQUAL_INT(ESP_OK, cred_store_save(&in));

    cred_store_t out;
    memset(&out, 0, sizeof(out));
    TEST_ASSERT_EQUAL_INT(ESP_OK, cred_store_load(&out));

    TEST_ASSERT_EQUAL_size_t(chain_sz, out.ca_chain_len);
    TEST_ASSERT_EQUAL_MEMORY(in.ca_chain, out.ca_chain, chain_sz);
}

/* --------------------------------------------------------------------------
 * Test 6: parse_not_after on a cert with truncated DER -> ESP_FAIL
 * -------------------------------------------------------------------------- */
static void test_parse_truncated_cert_returns_fail(void)
{
    uint64_t epoch = 0;
    /* Feed only the first half of the cert DER */
    esp_err_t err = cred_store_parse_not_after(fixture_cert_der,
                                               fixture_cert_der_len / 2,
                                               &epoch);
    TEST_ASSERT_NOT_EQUAL(ESP_OK, err);
}

/* --------------------------------------------------------------------------
 * Test 7: double save overwrites previous data
 * -------------------------------------------------------------------------- */
static void test_double_save_overwrites_with_real_cert(void)
{
    /* First save: fake data */
    cred_store_t a;
    memset(&a, 0, sizeof(a));
    a.dev_key_len  = 1; a.dev_key[0]  = 0xDE;
    a.dev_cert_len = 1; a.dev_cert[0] = 0xAD;
    a.ca_chain_len = 1; a.ca_chain[0] = 0xBE;
    a.not_after    = 0;
    TEST_ASSERT_EQUAL_INT(ESP_OK, cred_store_save(&a));

    /* Second save: real cert+key */
    cred_store_t b;
    memset(&b, 0, sizeof(b));
    b.dev_cert_len = fixture_cert_der_len;
    memcpy(b.dev_cert, fixture_cert_der, fixture_cert_der_len);
    b.dev_key_len  = fixture_key_der_len;
    memcpy(b.dev_key, fixture_key_der, fixture_key_der_len);
    b.ca_chain_len = 2; b.ca_chain[0] = 0xCA; b.ca_chain[1] = 0xFE;
    b.not_after    = FIXTURE_NOT_AFTER_EPOCH;
    TEST_ASSERT_EQUAL_INT(ESP_OK, cred_store_save(&b));

    cred_store_t out;
    memset(&out, 0, sizeof(out));
    TEST_ASSERT_EQUAL_INT(ESP_OK, cred_store_load(&out));

    TEST_ASSERT_EQUAL_size_t(fixture_cert_der_len, out.dev_cert_len);
    TEST_ASSERT_EQUAL_MEMORY(fixture_cert_der, out.dev_cert, fixture_cert_der_len);
    TEST_ASSERT_EQUAL_UINT64(FIXTURE_NOT_AFTER_EPOCH, out.not_after);
}

/* --------------------------------------------------------------------------
 * main
 * -------------------------------------------------------------------------- */
int main(void)
{
    UNITY_BEGIN();
    RUN_TEST(test_parse_not_after_fake_ndes_ca_cert);
    RUN_TEST(test_save_load_real_cert_and_key);
    RUN_TEST(test_clear_after_real_cert_save);
    RUN_TEST(test_parse_not_after_is_pure_function);
    RUN_TEST(test_large_ca_chain_preserved);
    RUN_TEST(test_parse_truncated_cert_returns_fail);
    RUN_TEST(test_double_save_overwrites_with_real_cert);
    return UNITY_END();
}
